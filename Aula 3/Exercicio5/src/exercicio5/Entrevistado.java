package exercicio5;

public class Entrevistado {
	private String sexo, resposta;

	public Entrevistado(String sexo, String resposta){
		this.sexo = sexo;
		this.resposta = resposta;
	}

	public String getSexo() {
		return sexo;
	}

	public String getResposta() {
		return resposta;
	}
}
